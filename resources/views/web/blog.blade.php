                    A pesar de lo que mucha gente piensa, las frutas no forman parte de la dieta natural de una cobaya. En la naturaleza se alimentan de heno, hierba y otras plantas pero no de frutas, así como tampoco de tubérculos ni de cereales.
                    Es por ello que el consumo de frutas debe ser reducido y no formar parte de su dieta diaria. Puedes incluirlas en su menú de vez en cuando, un par de veces a la semana, o utilizarlas a modo de premio. Las frutas tienen un alto contenido en azúcar que puede ser perjudicial para la salud de tu cobaya. Además, también tienen un alto contenido en agua y si te excedes con ellas pueden causar diarreas.
                    Las mejores frutas que pueden comer las cobayas son la manzana, la piña, el kiwi, la sandía y las fresas. 
